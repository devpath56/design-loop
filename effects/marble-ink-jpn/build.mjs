/**
 * Build the classic-script bundle: dist/marble-ink.global.js
 *
 * Why this exists: the design loop grades standalone HTML opened over file://,
 * where the origin is `null` and ES-module imports are CORS-blocked outright.
 * A classic <script src> has no such restriction. So prototypes load the global
 * build; real apps with a bundler use src/index.js.
 *
 *   node effects/marble-ink-jpn/build.mjs
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const src = (f) => readFileSync(join(here, 'src', f), 'utf8');

/** Strip ESM syntax so the modules can be concatenated into one scope. */
const flatten = (code) => code
  .replace(/^import[\s\S]*?from\s+['"][^'"]+['"];?\s*$/gm, '')   // import ... from '...'
  .replace(/^export\s+default\s+\w+;?\s*$/gm, '')                 // export default X;
  .replace(/^export\s*\{[^}]*\}\s*(from\s+['"][^'"]+['"])?;?\s*$/gm, '') // export { ... }
  .replace(/^export\s+(?=(const|class|function|let))/gm, '')      // export const/class/function
  .trim();

const banner = `/*! marble-ink-jpn — classic-script build. Generated by build.mjs; do not edit.
 *  MIT. Exposes window.MarbleInk and window.MarbleInkPresets. */`;

const bundle = `${banner}
(function (global) {
  'use strict';

${flatten(src('shader.js'))}

${flatten(src('presets.js'))}

${flatten(src('marble-ink.js'))}

  global.MarbleInk = MarbleInk;
  global.MarbleInkPresets = presets;
  global.MarbleInkPresetNames = presetNames;
})(typeof globalThis !== 'undefined' ? globalThis : window);
`;

mkdirSync(join(here, 'dist'), { recursive: true });
const out = join(here, 'dist', 'marble-ink.global.js');
writeFileSync(out, bundle);
console.log(`built ${out} — ${(bundle.length / 1024).toFixed(1)}kb`);
